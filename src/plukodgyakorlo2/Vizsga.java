    public void population() {
        try {
            FileInputStream file = new FileInputStream("src\\segedlet\\TermekKodparos.dat");
            ObjectInputStream oInput = new ObjectInputStream(file);

            boolean endOfFile = false;//=ture

            try {
                while (!endOfFile) {//()endOfFile
                    termekek.add(oInput.readObject());
                }
            } catch (IOException ex) {
                endOfFile = true;//false
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void tesztsorFeltolto(ArrayList<TermekKodParos> lista) {
        Random rd = new Random();
        tizeslista = new ArrayList<>();

        do {
            int vmi = rd.nextInt(termekek.size());

            if (tizeslista.isEmpty()
                    || !tizeslista.contains(lista.get(vmi))) {
                egytermeknev = lista.get(vmi).getTermeknev();
                egytermekkod = lista.get(vmi).getTermekkod();
                TermekKodParos uj = new TermekKodParos(egytermeknev, egytermekkod);
                tizeslista.add(uj);
                System.out.println(uj.getTermeknev() + uj.getTermekkod());

            }
        } while (tizeslista.size() < 10);
        for (int i = 0; i < tizeslista.size(); i++) {
            System.out.println(tizeslista.get(i).getTermeknev());
        }

    }


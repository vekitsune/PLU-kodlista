    private void tippFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tippFieldActionPerformed
kerdessorszamField.setText(""+kerdesSorszama);
        String begepelt = tippField.getText();
        if (begepelt.equalsIgnoreCase(tizeslista.get(kerdesSorszama).getTermekkod())) {
            joValaszokSzama++;
        }
        kerdesSorszama++;
        if (kerdesSorszama ==10) {
            endtime=System.nanoTime();
            switch (joValaszokSzama) {
                case 10: {
                    sz100Sz++;
                    break;
                }
                case 9: {
                    sz90tolSz++;
                    break;
                }
                case 8: {
                    sz80tolSz++;
                    break;
                }
                case 7: {
                    sz70tolSz++;
                    break;
                }
                default:
                    sikertelenSz++;

                    break;
            }
            eredmenykiiroFrissitese();
            joValaszokSzama=0;
            kerdesSorszama=0;
            startButtonActionPerformed(evt);
            
 
        }
        else{
             termeknevField.setText(tizeslista.get(kerdesSorszama).getTermeknev());
        }tippField.setText("");
       
       
    }//GEN-LAST:event_tippFieldActionPerformed

